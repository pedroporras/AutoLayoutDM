# ------------------------------
# 1) Dataset (tokens already discrete)
# ------------------------------
class LayoutTokenDataset(Dataset):
    """
    Each item:
      tokens:   LongTensor [M, 5]  (c,x,y,w,h) already discrete + padded
      pad_mask: BoolTensor [M, 5]  True where token == PAD
    """
    def __init__(self, token_tensors: torch.LongTensor, pad_token_id: int):
        self.x = token_tensors
        self.pad_id = pad_token_id

    def __len__(self):
        return self.x.size(0)

    def __getitem__(self, idx):
        tokens = self.x[idx]
        pad_mask = (tokens == self.pad_id)
        return tokens, pad_mask


def load_or_make_dataset(cfg: TrainConfig, vocab_meta: Dict[str, Dict[str, int]]) -> Tuple[Dataset, Dataset]:
    """
    Dummy dataset so the notebook runs.
    Replace this with real tokenized RICO / PubLayNet:
      - tokens are already KMeans-quantized for x,y,w,h
      - padded to M
      - include [MASK] and [PAD] in each modality vocab
    """
    torch.manual_seed(0)

    N_train = 512
    N_val = 128
    M = cfg.M

    # We'll build random tokens in a safe range (not using MASK/PAD)
    # For demo only.
    def make_split(N):
        split = torch.zeros((N, M, 5), dtype=torch.long)
        for a, m in enumerate(["c", "x", "y", "w", "h"]):
            V = vocab_meta[m]["vocab_size"]
            mask_id = vocab_meta[m]["mask_id"]
            pad_id = vocab_meta[m]["pad_id"]

            # sample from [0..V-1] excluding mask/pad (demo)
            choices = [i for i in range(V) if i not in (mask_id, pad_id)]
            split[:, :, a] = torch.tensor(
                torch.randint(0, len(choices), (N, M)),
                dtype=torch.long
            )
            split[:, :, a] = torch.take(torch.tensor(choices), split[:, :, a])

        # simulate variable length: last 5 slots are PAD
        pad_id_c = vocab_meta["c"]["pad_id"]
        split[:, -5:, :] = pad_id_c
        return split

    train_tokens = make_split(N_train)
    val_tokens = make_split(N_val)

    pad_id = vocab_meta["c"]["pad_id"]
    return LayoutTokenDataset(train_tokens, pad_id), LayoutTokenDataset(val_tokens, pad_id)


