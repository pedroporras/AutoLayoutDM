def main():
    cfg = TrainConfig(M=25)
    print("Device:", cfg.device)

    # You MUST align these IDs with your tokenization.
    # Each modality vocab includes:
    #   normal tokens + [MASK] + [PAD]
    vocab_meta = {
        "c": {"vocab_size": 30, "mask_id": 28, "pad_id": 29},  # example only
        "x": {"vocab_size": 66, "mask_id": 64, "pad_id": 65},  # example only
        "y": {"vocab_size": 66, "mask_id": 64, "pad_id": 65},
        "w": {"vocab_size": 66, "mask_id": 64, "pad_id": 65},
        "h": {"vocab_size": 66, "mask_id": 64, "pad_id": 65},
    }
    vocab_sizes = {m: meta["vocab_size"] for m, meta in vocab_meta.items()}

    # Data
    train_ds, val_ds = load_or_make_dataset(cfg, vocab_meta)
    train_loader = DataLoader(train_ds, batch_size=16, shuffle=True, drop_last=True)
    val_loader = DataLoader(val_ds, batch_size=16, shuffle=False, drop_last=False)

    # Model
    model = LayoutDMDenoiser(cfg, vocab_sizes).to(cfg.device)
    opt = torch.optim.AdamW(model.parameters(), lr=cfg.lr, betas=(cfg.beta1, cfg.beta2), weight_decay=cfg.weight_decay)

    # Precompute diffusion matrices
    device = torch.device(cfg.device)
    Qts_all, Qbars_all = precompute_Q_mats(cfg, vocab_meta, device)

    # Train (paper: 50 epochs for RICO, 20 for PubLayNet)
    epochs = 1
    for ep in range(epochs):
        print(f"\n=== Epoch {ep+1}/{epochs} ===")
        train_one_epoch(cfg, model, train_loader, opt, Qts_all, Qbars_all, vocab_meta)

    # Save checkpoint
    ckpt_path = "layoutdm_minimal.pt"
    torch.save({
        "cfg": cfg.__dict__,
        "model_state": model.state_dict(),
        "vocab_meta": vocab_meta,
    }, ckpt_path)
    print("Saved:", ckpt_path)

    # Unconditional sampling demo
    samples = unconditional_sample(model, cfg, vocab_meta, batch_size=4)
    print("Sample tokens shape:", samples.shape)  # [B,M,5]
    print("First sample (first 5 elements):\n", samples[0, :5, :])

main()

